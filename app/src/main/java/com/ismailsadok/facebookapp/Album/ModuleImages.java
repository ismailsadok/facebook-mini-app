package com.ismailsadok.facebookapp.Album;



public class ModuleImages {
    private int height;
    private int width;
    private String source;

    public int getHeight() {
        return height;
    }

    public ModuleImages setHeight(int height) {
        this.height = height;
        return this;
    }

    public int getWidth() {
        return width;
    }

    public ModuleImages setWidth(int width) {
        this.width = width;
        return this;
    }

    public String getSource() {
        return source;
    }

    public ModuleImages setSource(String source) {
        this.source = source;
        return this;
    }
}
